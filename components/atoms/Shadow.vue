<template>
  <div
    :style="{
      'text-shadow': _textShadow,
    }"
    :class="shadow"
  >
    <slot></slot>
  </div>
</template>
<script setup lang="ts">
const props = defineProps({
  type: { default: "basic" },
  textShadow: { type: String, default: "rgba(0, 0, 0, 0.3)" },
  color: { default: "rgba(0, 0, 0, 0.5)" },
  background: { default: "rgba(50, 50, 50);" },
});

/// A text-shadow shim because Tailwind doesn't support it.
const _textShadow = computed(() => {
  let { color, textShadow, type } = props;
  switch (type.toLowerCase()) {
    // case "letterpress":
    //   return `2px 2px 3px ${textShadow || "rgba(255, 255, 255, 0.1)"}`;
    case "basic":
      return `4px 6px 8px ${textShadow}`;
    case "distant":
      return `0px 3px 0px ${textShadow}`;
    default:
      return `${type}`;
  }
});

// Normal computed Tailwind Styles :)
const shadow = computed(() => {
  let { color, textShadow, type, background } = props;
  switch (type.toLowerCase()) {
    default:
      return type.toLowerCase();
  }
});
</script>

